
public class Variables 
{
	public static void main(String[] args) 
	{
		/*
		 * A variable is a location in memory that stores some data
		 * 
		 * A variable has both a type and a name
		 * 
		 * The type specifies what sort of data it will refer to
		 * The name is how it can be referenced in code
		 * 
		 * A variable must be declared before it is used
		 * 
		 * A variable declaration statement:
		 * 		type name;
		 * 
		 * Examples:
		 * 		int age; //integer
		 * 		double y; // floating point number (had decimal)
		 * 		String school; // a sequence of characters
		 * 
		 * Assignment statements:
		 * 		variable = value;
		 * 
		 * Examples:
		 * 		age = 17;
		 * 		y = -3.75;
		 * 		school = "phs";
		 * 
		 * Equal sign does not mean what it means in math
		 * In java it means take the value and put in the variable
		 * 
		 * Declaration/Assignment combination:
		 * 		int age = 17;
		 * 		doubly y = -3.75;
		 * 		String school = "phs";
		 * 
		 * A variable is only declared once
		 * After that it can be used as many time as you want
		 * A variable cannot be declared again
		 * 		int age = 17;
		 * 		// do some stuff
		 * 		age = 3;
		 * 		// do some stuff
		 * 		age = 90;
		 * 
		 * Only declare it once
		 * 
		 * Once declared and given a value, a variable can be used in any situation and in place of it
		 */
		int age = 17;
		System.out.println(age); // prints out 17
		age = 13;
		System.out.println(age); // prints out 13
		
		/*
		 * Data types:
		 * 
		 * There are two categories of data types in Java:
		 * 1) Primitive types
		 * 2) Object types
		 * 
		 * Primitive Types:
		 * 1) byte
		 * 2) short
		 * 3) int *
		 * 4) long
		 * 5) float
		 * 6) double *
		 * 7) char
		 * 8) boolean * 
		 * 
		 * Object types - everything else
		 * 		Capitalized
		 * 
		 * 
		 */
	}
}



























