
public class Printing 
{
	public static void main(String[] args) 
	{
		System.out.println("Kathmandu");
		// Single line comment
		/*
		 * Multiple
		 * Line
		 * Comment
		 */
		/*
		 * System.out.print(x) will print x to the screen
		 * System.out.println(x) will print x to the screen
		 * 		then go on a new line
		 */
		System.out.print("My name is Mr.Hitron");
		System.out.println("Hello");
		System.out.print("to you all");
	}
}
